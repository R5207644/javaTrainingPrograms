package encapsulation;


public class Student {
    int roll;
    String name;
    String course;
    
    public void setRoll(int roll) {
        this.roll = roll;
    }
    
    public int getRoll() {
        return roll;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public String getName() {
        return name;
    }
    
    public void setCourse(String course) {
        this.course = course;
    }
    
    public String getCourse() {
        return course;
    }
    
}
